from random import choice


class PricingStorage(dict):
    def add(self, flavor, option_id, managed, region, price):
        self[(flavor, option_id, managed, region)] = price

    def get_price(self, flavor, option_id, managed, region):
        return self[(flavor, option_id, managed, region)]

    def get_keys(self, flavor=None, option_id=None, managed=None, region=None):
        found = self.keys()
        if flavor:
            found = [key for key in found if key[0] == flavor]
        if option_id:
            found = [key for key in found if key[1] == option_id]
        if managed:
            found = [key for key in found if key[2] == managed]
        if region:
            found = [key for key in found if key[3] == region]
        return found

    def get_random(self):
        return choice(self.keys())


